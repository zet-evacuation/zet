package zet.gui.main.tabs.base;

/**
 * An enumeration describing all possible styles for painting a raster on the
 * editor background.
 */
public enum RasterPaintStyle {
	/** Draw a grid. */
	Lines,
	/** Draw points at the nodes of the grid. */
	Points,
	/** Do not draw anything. */
	Nothing;
}
